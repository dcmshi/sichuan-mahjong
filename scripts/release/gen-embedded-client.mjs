// Generate packages/server/src/generated/embedded-client.ts from the built client
// SPA (packages/client/dist). The Bun binary entry (binary.ts) imports it and hands
// it to the server so a standalone binary serves the UI without a client dir on
// disk. Output is gitignored + Bun-only; run from scripts/release/compile.ts. (A20)
import { existsSync, mkdirSync, readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, extname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repo = join(here, '..', '..');
const clientDist = join(repo, 'packages', 'client', 'dist');
const outDir = join(repo, 'packages', 'server', 'src', 'generated');
const outFile = join(outDir, 'embedded-client.ts');

const CONTENT_TYPES = {
  '.html': 'text/html; charset=utf-8',
  '.js': 'text/javascript; charset=utf-8',
  '.css': 'text/css; charset=utf-8',
  '.svg': 'image/svg+xml',
  '.json': 'application/json; charset=utf-8',
  '.webmanifest': 'application/manifest+json; charset=utf-8',
  '.png': 'image/png',
  '.ico': 'image/x-icon',
  '.woff2': 'font/woff2',
  '.woff': 'font/woff',
  '.txt': 'text/plain; charset=utf-8',
};

function walk(dir, acc = []) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) walk(full, acc);
    else acc.push(full);
  }
  return acc;
}

if (!existsSync(clientDist)) {
  console.error(`[gen-embedded-client] client build not found at ${clientDist}`);
  console.error('Build it first: pnpm --filter @sichuan-mahjong/client build');
  process.exit(1);
}

const files = walk(clientDist);
const map = {};
for (const full of files) {
  const urlPath = `/${relative(clientDist, full).split(/[\\/]/).join('/')}`;
  const type = CONTENT_TYPES[extname(full).toLowerCase()] ?? 'application/octet-stream';
  map[urlPath] = { type, body: readFileSync(full).toString('base64') };
}

mkdirSync(outDir, { recursive: true });
const src = `// AUTO-GENERATED by scripts/release/gen-embedded-client.mjs — do not edit.
// Client SPA (packages/client/dist) as URL path -> { content-type, base64 }.
// Embedded into the Bun binary via binary.ts. Bun-only; excluded from tsc. (A20)
import type { EmbeddedClient } from '../http.js';

export const embeddedClient: EmbeddedClient = ${JSON.stringify(map)};
`;
writeFileSync(outFile, src);
console.log(`[gen-embedded-client] embedded ${files.length} files → ${outFile}`);
